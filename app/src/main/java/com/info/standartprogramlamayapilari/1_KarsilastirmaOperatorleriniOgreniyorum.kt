package com.info.standartprogramlamayapilari

fun main(){

    var s1 = 60
    var s2 = 50

    var y1 = 70
    var y2 = 80

    //Karsilatirma Operatorleri
    println(s1 == s2) // Sonuc : False
    println(s1 != s2) // Sonuc : True
    println(s1 > s2) // Sonuc : True
    println(s1 >= s2) // Sonuc : True
    println(s1 < s2) // Sonuc : False
    println(s1 <= s2) // Sonuc : False

    //Mantiksal Operatorler
    println(s1>s2 || y1>y2) // Sonuc : True
    println(s1>s2 && y1>y2) // Sonuc : False

}